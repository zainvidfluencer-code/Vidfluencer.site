export function formatCurrency(amountUSD: number, locale: LocaleCode) {
  const rates: Record<LocaleCode, { rate: number; currency: string; symbol: string }> = {
    'en-US': { rate: 1, currency: 'USD', symbol: '$' },
    'en-GB': { rate: 0.8, currency: 'GBP', symbol: '£' },
    'es-ES': { rate: 0.9, currency: 'EUR', symbol: '€' },
    'it-IT': { rate: 0.9, currency: 'EUR', symbol: '€' },
    'fr-FR': { rate: 0.9, currency: 'EUR', symbol: '€' },
    'en-AU': { rate: 1.5, currency: 'AUD', symbol: 'A$' },
    'en-CA': { rate: 1.4, currency: 'CAD', symbol: 'C$' },
  };
  
  const { rate, currency } = rates[locale];
  const converted = amountUSD * rate;
  
  return new Intl.NumberFormat(locale, {
    style: 'currency',
    currency: currency,
    maximumFractionDigits: 0,
  }).format(converted);
}

